void pre()
{

   // TODO add your handling code here:
        if(("".equals(min.getText())||("".equals(max.getText() ))))
        {
        JOptionPane.showMessageDialog(null,"Veuillez remplir le min et max de votre pression!");
        
        }
        else
        {
        Health h =new Health();
        try
        {
        h.setMin(Integer.parseInt(min.getText()));
        h.setMax(Integer.parseInt(max.getText()));
JOptionPane.showMessageDialog(null, h.tension());
        }
        catch(HeadlessException | NumberFormatException ex)
        {
        JOptionPane.showMessageDialog(null,"veuillez saisir des valeurs numérique");
        }
        
        }

}
