void gly()
{
    Health h=new Health();
        if("".equals(g.getText()))
        {
        JOptionPane.showMessageDialog(null, "veuillez saisir la glycémie");
        
        }
        else
        {
        try
        {
        h.setGlycemie(Double.parseDouble(g.getText()));
        JOptionPane.showMessageDialog(null, "votre glycémie est: "+h.gly());
        }
        catch(Exception ex)
        {
                JOptionPane.showMessageDialog(null, "veuillez saisir une valeur numérique");

        }
        
        
        }


}
